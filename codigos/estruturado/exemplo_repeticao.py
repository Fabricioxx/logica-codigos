# Exemplo 2: Estruturas de repetição
# Laço for: repete de 1 até 5
for i in range(1, 6):
    print(f'Contagem: {i}')  # Mostra o valor de i a cada repetição

# Laço while: repete enquanto a condição for verdadeira
contador = 0
while contador < 5:
    print(f'While: {contador}')  # Mostra o valor do contador
    contador += 1  # Soma 1 ao contador a cada volta
